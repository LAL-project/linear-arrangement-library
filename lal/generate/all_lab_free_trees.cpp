#include <lal/generate/all_lab_free_trees.hpp>

// C++ includes
#include <limits>

// lal includes
#include <lal/detail/graphs/conversions.hpp>

namespace lal {
namespace generate {

/* PROTECTED */

graphs::free_tree all_lab_free_trees::__get_tree() noexcept {
	if (m_n <= 1) { return graphs::free_tree(m_n); }
	if (m_n == 2) {
		graphs::free_tree t(2);
		t.set_edges(std::vector<edge>{edge(0,1)});
		return t;
	}
	return detail::Prufer_sequence_to_ftree(m_Prufer_seq.begin(), m_n, false, false);
}

} // -- namespace generate
} // -- namespace lal

