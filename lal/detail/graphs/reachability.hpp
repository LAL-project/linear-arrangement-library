#pragma once

// lal includes
#include <lal/detail/graphs/traversal.hpp>

namespace lal {
namespace detail {

/**
 * @brief Is a node reachable from another?
 * @param g Input graph.
 * @param source Node where the search starts at.
 * @param target The node we want to know whether it is reachable from
 * @e source or not.
 * @returns True if, and only if, node target is reachable from node source.
 */
template<class graph_t>
bool is_node_reachable_from(const graph_t& g, const node source, const node target)
noexcept
{
	BFS<graph_t> bfs(g);
	bfs.set_terminate
	([target](const auto&, const node s) -> bool { return (s == target); });
	bfs.start_at(source);
	return bfs.node_was_visited(target);
}

} // -- namespace detail
} // -- namespace lal
